void throw()
{
	servo[rightServo] = rightLaunchPos;
	servo[leftServo] = leftLaunchPos;
	wait1Msec(500);

	motor[armMotor] = 100;
	wait1Msec(800);
	motor[armMotor] = -12;
	wait1Msec(1500);
	motor[armMotor] = 0;
}

void forwards()
{
	motor[armMotor] = 30;
	wait1Msec(1000);
	motor[armMotor] = 0;
}

void backwards()
{
	motor[armMotor] = -10;
	wait1Msec(500);
	motor[armMotor] = 0;
}






task main()
{
	ClearTimer(T1);
	initializeRobot();


//	waitForStart();   // wait for start of tele-op phase

	while (true)
	{
		if(turnSpinner == true)
		{
			motor[lawnMower] = 100;
		}
		else
		{
			motor[lawnMower] = 0;
		}

		if ((abs(joystick.joy1_x1) > THRESHOLD) || (abs(joystick.joy1_y1) > THRESHOLD))
		{
			motor[leftWheel] =  (((float)joystick.joy1_x1/MOTOR_SCALAR) - ((float)joystick.joy1_y1/MOTOR_SCALAR))*100;
			motor[rightWheel] = (((float)joystick.joy1_x1/MOTOR_SCALAR) + ((float)joystick.joy1_y1/MOTOR_SCALAR))*100;
		}
		else
		{
			motor[leftWheel] = 0;
			motor[rightWheel] = 0;
		}


		if (joy1Btn(9))
		{
			adjustDistanceToSmall();
			throw();
		}

		 if (joy1Btn(3))
		{
			adjustDistancetoMedium();
			throw();
		}

		 if (joy1Btn(4))
		{
			adjustDistancetoLarge();
			throw();
		}

		 if (joy1Btn(8))
		{
			backwards();
		}

		 if (joy1Btn(6))
		{
			forwards();
		}

		if (joy1Btn(5))
		{
			if (servo[leftServo] == 100)
			{
				servo[leftServo] = 50;
			}
			else
			{
				servo[leftServo] = 100;
			}

		}

		if (joy1Btn(6))
		{
			if (servo[rightServo] == 100)
			{
				servo[rightServo] = 50;
			}
			else
			{
				servo[rightServo] = 100;
			}
		}

		if (joy1Btn(2))
		{
			if (time1(T1) > 500)
			{
				ClearTimer(T1);
				if (turnSpinner == true)
				{
					turnSpinner = false;
				}
				else
				{
					turnSpinner = true;
				}
			}
		}

	}

}
